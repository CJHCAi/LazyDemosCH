+ (NSString *)reverseString:(NSString *)strSrc
{
    NSMutableString* reverseString = [[NSMutableString alloc] init];
    NSInteger charIndex = [strSrc length];
    while (charIndex > 0) {
        charIndex --;
        NSRange subStrRange = NSMakeRange(charIndex, 1);
        [reverseString appendString:[strSrc substringWithRange:subStrRange]];
    }
    return reverseString;
}

+ (CGFloat)heightWithLabelSizeToFitContent:(NSString *)content numberOfLines:(NSInteger)line constrainedToWidth:(CGFloat)width {
    NSMutableParagraphStyle *paragraphStyle = [[NSMutableParagraphStyle alloc] init];
    [paragraphStyle setLineSpacing:7];
    NSAttributedString *attributedString = [[NSAttributedString alloc]initWithString:content attributes:@{NSParagraphStyleAttributeName : paragraphStyle}];
 
    UILabel *contentLabel = [[UILabel alloc]initWithFrame:CGRectMake(0, 0, width, 0)];
    contentLabel.font = [UIFont systemFontOfSize:11];
    contentLabel.numberOfLines = line;
    contentLabel.attributedText = attributedString;
    [contentLabel sizeToFit];
    
    return CGRectGetHeight(contentLabel.frame);
}

+ (BOOL)content:(NSString *)content beyondMaxLine:(NSInteger)line constrainedToWidth:(CGFloat)width{
    CGFloat contentHeight = [self heightWithLabelSizeToFitContent:content numberOfLines:0 constrainedToWidth:width];
    CGFloat maxLineHeight = [self heightWithLabelSizeToFitContent:content numberOfLines:line constrainedToWidth:width];
    if (contentHeight > maxLineHeight) {
        return YES;
    }
    return NO;
}

